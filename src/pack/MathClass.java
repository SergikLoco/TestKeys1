package pack;

public  class MathClass {

    public static float Sum(float x, float y)
    {
        return x+y;
    }
    public static float Dif(float x, float y)
    {
        return x-y;
    }
    public static float Multi(float x, float y)
    {
        return x*y;
    }
    public static float Div(float x, float y)
    {
        return x/y;
    }
}
